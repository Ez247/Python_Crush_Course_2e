"""_Intro to Loops_:
        0. for loops:
            a. takes a collection of items
            b. executes a block of code once for each item in the collection.
        1. while loop:
            a. runs as long as, or while a certain condition is true           
"""

"""_While loop in action_:
"""
#start counting from 1
current_number = 1          

#loop through as long as current_number is less than or equal to 5
while current_number <= 5:  
    print(current_number) 

    #print current_number and add 1 to it
    current_number += 1     
